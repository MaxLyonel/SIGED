<?php

namespace Sie\AppWebBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * ParaleloTipo
 */
class ParaleloTipo
{
    /**
     * @var string
     */
    private $id;

    /**
     * @var string
     */
    private $paralelo;

    /**
     * @var string
     */
    private $obs;

    public function __toString() {
        return $this->paralelo;
    }

    /**
     * Get id
     *
     * @return string 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set paralelo
     *
     * @param string $paralelo
     * @return ParaleloTipo
     */
    public function setParalelo($paralelo)
    {
        $this->paralelo = $paralelo;

        return $this;
    }

    /**
     * Get paralelo
     *
     * @return string 
     */
    public function getParalelo()
    {
        return $this->paralelo;
    }

    /**
     * Set obs
     *
     * @param string $obs
     * @return ParaleloTipo
     */
    public function setObs($obs)
    {
        $this->obs = $obs;

        return $this;
    }

    /**
     * Get obs
     *
     * @return string 
     */
    public function getObs()
    {
        return $this->obs;
    }
}
