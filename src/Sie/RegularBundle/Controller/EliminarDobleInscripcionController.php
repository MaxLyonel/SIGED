    public function eliminarAction(Request $request){
        $em = $this->getDoctrine()->getManager();
        $response = new JsonResponse();
        $idInscripcion = $request->get('id');
        $inscripcion = $em->getRepository('SieAppWebBundle:EstudianteInscripcion')->find($idInscripcion);
        if (is_object($inscripcion)) {
            $estudiante = $em->getRepository('SieAppWebBundle:Estudiante')->find($inscripcion->getEstudiante());
            $em->remove($inscripcion);
            $em->flush();

            // mas inscripciones
            $inscripciones = $em->getRepository('SieAppWebBundle:EstudianteInscripcion')->findBy(['estudiante'=>$estudiante->getId()]);
            if (count($inscripciones) == 0) {
                $em->remove($estudiante);
                $em->flush();
                $response->setData([
                    'status'=>'success',
                    'msg'=>'El estudiante y la inscripcin fueron eliminados'
                ]);
            }else{
                $response->setData([
                    'status'=>'success',
                    'msg'=>'La inscripcin fue eliminada, el estudiante aun tiene inscripciones con este rude'
                ]);
            }
        }else{
            $response->setData([
                'status'=>'error',
                'msg'=>'La inscripcin no existe'
            ]);
        }

        return $response;
    }
